
$("#filter_option1").change(function()
{
	if($(this).val() == "1"){
		$("#position_value1").show();
	} else {
		$("#position_value1").hide();
	}
	if($(this).val() == "2"){
		$("#salary_value1").show();
	} else {
		$("#salary_value1").hide();
	}
	if($(this).val() == "3"){
		$("#age_value1").show();
	} else {
		$("#age_value1").hide();
	}
	if($(this).val() == "4"){
		$("#gender_value1").show();
	} else {
		$("#gender_value1").hide();
	}
	if($(this).val() == "5"){
		$("#nationality_value1").show();
	} else {
		$("#nationality_value1").hide();
	}
	if($(this).val() == "6"){
		$("#status_value1").show();
	} else {
		$("#status_value1").hide();
	}
	if($(this).val() == "7"){
		$("#edu_value1").show();
	} else {
		$("#edu_value1").hide();
	}
	if($(this).val() == "8"){
		$("#university_value1").show();
	} else {
		$("#university_value1").hide();
	}
	if($(this).val() == "9"){
		$("#faculty_value1").show();
	} else {
		$("#faculty_value1").hide();
	}


});
$("#position_value1").hide();
$("#salary_value1").hide();
$("#age_value1").hide();
$("#gender_value1").hide();
$("#nationality_value1").hide();
$("#status_value1").hide();
$("#edu_value1").hide();
$("#university_value1").hide();
$("#faculty_value1").hide();
